#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((char)str[i] == (char)c)
			return ((char*)(str + i));
		i++;
	}
	return (0);
}
